#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
RESULT_DIR="$ROOT_DIR/evaluation/results"
RESULT_FILE="$RESULT_DIR/jact-vs-javafx-baseline.md"

mkdir -p "$RESULT_DIR"

count_non_empty_lines() {
  local file
  local total=0
  for file in "$@"; do
    if [[ -f "$file" ]]; then
      local count
      count="$(grep -v '^[[:space:]]*$' "$file" | wc -l | tr -d ' ')"
      total=$((total + count))
    fi
  done
  echo "$total"
}

count_markers() {
  local pattern="$1"
  shift
  local count=0
  local file
  for file in "$@"; do
    if [[ -f "$file" ]]; then
      local matches
      matches="$(grep -E "$pattern" "$file" | wc -l | tr -d ' ' || true)"
      count=$((count + matches))
    fi
  done
  echo "$count"
}

JACT_UI_FILES=(
  "$ROOT_DIR/samples-task-manager/src/main/java/io/jact/sample/HomePage.java"
  "$ROOT_DIR/samples-task-manager/src/main/java/io/jact/sample/pages/tasks/\$id/TaskDetailPage.java"
  "$ROOT_DIR/samples-task-manager/src/main/java/io/jact/sample/tasks/TaskComponents.java"
)

BASELINE_UI_FILES=(
  "$ROOT_DIR/samples-task-manager-javafx-baseline/src/main/java/io/jact/baseline/TaskManagerBaselineApplication.java"
  "$ROOT_DIR/samples-task-manager-javafx-baseline/src/main/java/io/jact/baseline/TaskManagerController.java"
)

BASELINE_FXML_FILES=(
  "$ROOT_DIR/samples-task-manager-javafx-baseline/src/main/resources/io/jact/baseline/task-manager.fxml"
)

JACT_LOC="$(count_non_empty_lines "${JACT_UI_FILES[@]}")"
BASELINE_JAVA_LOC="$(count_non_empty_lines "${BASELINE_UI_FILES[@]}")"
BASELINE_FXML_LOC="$(count_non_empty_lines "${BASELINE_FXML_FILES[@]}")"

SYNC_PATTERN='addListener|refreshList|setText|setSelected|setDisable|setItems|setValue|getItems\(\)\.setAll'
JACT_SYNC="$(count_markers "$SYNC_PATTERN" "${JACT_UI_FILES[@]}")"
BASELINE_SYNC="$(count_markers "$SYNC_PATTERN" "${BASELINE_UI_FILES[@]}")"

cat > "$RESULT_FILE" <<REPORT
# JACT vs JavaFX MVC/FXML Baseline Metrics

Generated by \`evaluation/run-metrics.sh\`.

| Metric | JACT Task Manager | JavaFX MVC/FXML Baseline |
| --- | ---: | ---: |
| UI Java LOC | $JACT_LOC | $BASELINE_JAVA_LOC |
| FXML LOC | 0 | $BASELINE_FXML_LOC |
| UI Java files | ${#JACT_UI_FILES[@]} | ${#BASELINE_UI_FILES[@]} |
| FXML files | 0 | ${#BASELINE_FXML_FILES[@]} |
| Manual UI synchronization markers | $JACT_SYNC | $BASELINE_SYNC |

## Interpretation Notes

- JACT expresses UI updates through state, stores, external observables, and component re-rendering.
- The JavaFX baseline performs explicit controller refresh and direct control mutation.
- These metrics should be interpreted together with the ergonomics rubric, not as a standalone universal benchmark.
REPORT

echo "Wrote $RESULT_FILE"
